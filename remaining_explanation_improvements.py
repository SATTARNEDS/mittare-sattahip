def improve_remaining_explanation(topic, question, options, answer):
    if topic not in TOPIC_SOURCES:
        return None
    context = f"{question} {answer}".casefold()
    if str(answer).startswith(("ถูกทุกข้อ", "ถูกทั้ง", "ถูกเฉพาะ")):
        context += " " + " ".join(options).casefold()
    scored = [(sum(key.casefold() in context for key in keys), reason) for keys, reason in RULES[topic]]
    score, reason = max(scored, key=lambda item: item[0])
    if not score:
        reason = "ต้องพิจารณานิยาม ข้อตกลงคุ้มครอง ข้อยกเว้น วงเงิน และข้อเท็จจริงในโจทย์ร่วมกัน จึงเลือกข้อความที่ตรงองค์ประกอบมากที่สุด"
    clean_answer = " ".join(str(answer).split())
    title, url = TOPIC_SOURCES[topic]
    return {"explanation": f"คำตอบที่ถูกคือ “{clean_answer}” เพราะ{reason}", "sourceTitle": title,
            "sourceUrl": url, "reviewStatus": "official_context_reviewed"}

def apply_remaining_review_batch(database, verified_at):
    topics = tuple(TOPIC_SOURCES)
    rows = database.execute(
        f"SELECT * FROM questions WHERE topic IN ({','.join('?' for _ in topics)}) "
        "AND (explanation LIKE 'ตามเฉลยที่ทำเครื่องหมายไว้ในเอกสารต้นฉบับ%' OR explanation LIKE 'ตามเฉลยในเอกสาร%') ORDER BY id",
        topics,
    ).fetchall()
    updated_ids = []
    for row in rows:
        improved = improve_remaining_explanation(row["topic"], row["question_text"], json.loads(row["options_json"]), row["correct_answer"])
        version = database.execute("SELECT COALESCE(MAX(version_number),0) n FROM question_versions WHERE question_id=?", (row["id"],)).fetchone()["n"]
        database.execute("INSERT INTO question_versions(question_id,version_number,snapshot_json,action,changed_by) VALUES (?,?,?,'remaining_explanation_reviewed',NULL)", (row["id"], version + 1, json.dumps(dict(row), ensure_ascii=False)))
        database.execute("UPDATE questions SET explanation=?,explanation_source_title=?,explanation_source_url=?,explanation_verified_at=?,explanation_review_status=?,updated_at=? WHERE id=?", (improved["explanation"], improved["sourceTitle"], improved["sourceUrl"], verified_at, improved["reviewStatus"], verified_at, row["id"]))
        updated_ids.append(row["id"])
    return {"updated": len(updated_ids), "ids": updated_ids}
