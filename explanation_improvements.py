def _select_reason(text: str, rules: Iterable[tuple[tuple[str, ...], str, str]] | Iterable[tuple[tuple[str, ...], str]]):
    lowered = text.casefold()
    best_rule = None
    best_score = 0
    for rule in rules:
        score = sum(1 for keyword in rule[0] if keyword.casefold() in lowered)
        if score > best_score:
            best_rule = rule[1:]
            best_score = score
    return best_rule


def _answer_lead(answer: str) -> str:
    clean_answer = " ".join(str(answer).split())
    if clean_answer.startswith(("ถูกทุกข้อ", "ถูกทั้ง", "ถูกเฉพาะ")):
        return f"คำตอบ “{clean_answer}” หมายถึงต้องพิจารณาข้อความในตัวเลือกที่ระบุร่วมกัน ไม่ควรจำเพียงตัวอักษรของตัวเลือก"
    return f"คำตอบที่ถูกคือ “{clean_answer}”"


def improve_explanation(topic: str, question: str, options: list[str], answer: str) -> dict | None:
    context = " ".join([question, answer])
    if str(answer).startswith(("ถูกทุกข้อ", "ถูกทั้ง", "ถูกเฉพาะ")):
        context = " ".join([context, *options])
    if topic == ETHICS_TOPIC:
        selected = _select_reason(context, ETHICS_RULES)
        reason = selected[0] if selected else (
            "หลักจรรยาบรรณกำหนดให้นายหน้าปฏิบัติอย่างซื่อสัตย์ เป็นธรรม มีความรู้เพียงพอ "
            "และคำนึงถึงสิทธิของลูกค้าตลอดกระบวนการเสนอขายและบริการหลังการขาย"
        )
        return {
            "explanation": f"{_answer_lead(answer)} เพราะ{reason} จึงสอดคล้องกับแนวปฏิบัติการเสนอขายของ คปภ.",
            "sourceTitle": ETHICS_SOURCE_TITLE, "sourceUrl": ETHICS_SOURCE_URL,
            "reviewStatus": "official_principle_reviewed",
        }
    if topic == LAW_TOPIC:
        selected = _select_reason(context, LAW_RULES)
        if selected:
            section, reason = selected
        else:
            section = "พระราชบัญญัติประกันวินาศภัย พ.ศ. 2535 ฉบับประมวล"
            reason = "สิทธิ หน้าที่ และอำนาจต้องเป็นไปตามบทบัญญัติเฉพาะของกฎหมาย ไม่อาจใช้ข้อตกลงหรือการปฏิบัติที่ขัดต่อกฎหมายได้"
        return {
            "explanation": f"{_answer_lead(answer)} เพราะ{reason} หลักนี้อยู่ใน{section} โดยควรอ่านฉบับประมวลเมื่อคำถามเกี่ยวกับตัวเลข ระยะเวลา หรือโทษโดยเฉพาะ",
            "sourceTitle": LAW_SOURCE_TITLE, "sourceUrl": LAW_SOURCE_URL,
            "reviewStatus": "official_context_reviewed",
        }
    return None


def apply_first_review_batch(database, verified_at: str, include_reviewed: bool = False) -> dict:
    """อัปเดตเฉพาะ placeholder เดิม พร้อมเก็บ snapshot ทุกข้อเพื่อย้อนกลับได้"""
    review_filter = "" if include_reviewed else (
        "AND (explanation LIKE 'ตามเฉลยที่ทำเครื่องหมายไว้ในเอกสารต้นฉบับ%' "
        "OR explanation LIKE 'ตามเฉลยในเอกสาร%')"
    )
    rows = database.execute(
        f"SELECT * FROM questions WHERE topic IN (?, ?) {review_filter} ORDER BY id",
        (ETHICS_TOPIC, LAW_TOPIC),
    ).fetchall()
    updated_ids = []
    for row in rows:
        improved = improve_explanation(
            row["topic"], row["question_text"], json.loads(row["options_json"]), row["correct_answer"]
        )
        if improved is None:
            continue
        last_version = database.execute(
            "SELECT COALESCE(MAX(version_number), 0) AS number FROM question_versions WHERE question_id=?",
            (row["id"],),
        ).fetchone()["number"]
        database.execute(
            """INSERT INTO question_versions(question_id, version_number, snapshot_json, action, changed_by)
            VALUES (?, ?, ?, ?, NULL)""",
            (row["id"], last_version + 1, json.dumps(dict(row), ensure_ascii=False),
             "explanation_rule_refined" if include_reviewed else "explanation_review_started"),
        )
        database.execute(
            """UPDATE questions SET explanation=?, explanation_source_title=?, explanation_source_url=?,
            explanation_verified_at=?, explanation_review_status=?, updated_at=? WHERE id=?""",
            (improved["explanation"], improved["sourceTitle"], improved["sourceUrl"], verified_at,
             improved["reviewStatus"], verified_at, row["id"]),
        )
        updated_ids.append(row["id"])
    return {"updated": len(updated_ids), "ids": updated_ids}
